/**
 * EditorTabs - Tab bar for ritual editor
 */
export function EditorTabs({ activeTab, onTabChange }: EditorTabsProps) {
  return (
    <div className="flex border-b border-calm-200 overflow-x-auto">
      {tabs.map((tab) => (
        <button
          key={tab.id}
          onClick={() => onTabChange(tab.id)}
          className={`flex items-center gap-2 px-4 py-3 text-sm font-medium whitespace-nowrap border-b-2 transition-colors ${
            activeTab === tab.id
              ? 'border-peach-500 text-peach-600'
              : 'border-transparent text-calm-600 hover:text-calm-800 hover:border-calm-300'
          }`}
          aria-selected={activeTab === tab.id}
          role="tab"
        >
          {tab.icon}
          <span className="hidden sm:inline">{tab.label}</span>
        </button>
      ))}
    </div>
  )
}
